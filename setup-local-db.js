#!/usr/bin/env node

/**
 * Local Database Setup Script for ChimariData
 * 
 * This script helps set up a local PostgreSQL database for development.
 * Run with: node setup-local-db.js
 */

import { execSync } from 'child_process';
import { readFileSync, writeFileSync } from 'fs';
import { join } from 'path';
import readline from 'readline';

const rl = readline.createInterface({
  input: process.stdin,
  output: process.stdout
});

function question(prompt) {
  return new Promise((resolve) => {
    rl.question(prompt, resolve);
  });
}

async function setupDatabase() {
  console.log('🚀 ChimariData Local Database Setup');
  console.log('=====================================\n');

  // Get database configuration from user
  const dbName = await question('Database name (default: chimaridata_dev): ') || 'chimaridata_dev';
  const dbUser = await question('Database user (default: postgres): ') || 'postgres';
  const dbPassword = await question('Database password (leave empty if none): ') || '';
  const dbHost = await question('Database host (default: localhost): ') || 'localhost';
  const dbPort = await question('Database port (default: 5432): ') || '5432';

  console.log('\n📝 Configuration Summary:');
  console.log(`Database: ${dbName}`);
  console.log(`User: ${dbUser}`);
  console.log(`Host: ${dbHost}:${dbPort}`);
  console.log(`Password: ${dbPassword ? '[SET]' : '[NONE]'}\n`);

  const confirm = await question('Proceed with setup? (y/N): ');
  if (confirm.toLowerCase() !== 'y') {
    console.log('Setup cancelled.');
    rl.close();
    return;
  }

  try {
    // Build connection string
    const connectionString = dbPassword 
      ? `postgresql://${dbUser}:${dbPassword}@${dbHost}:${dbPort}/${dbName}`
      : `postgresql://${dbUser}@${dbHost}:${dbPort}/${dbName}`;

    console.log('\n🔧 Setting up database...');

    // Try to create database
    const createDbCommand = dbPassword
      ? `"C:\\Program Files\\PostgreSQL\\17\\bin\\createdb.exe" -h ${dbHost} -p ${dbPort} -U ${dbUser} ${dbName}`
      : `"C:\\Program Files\\PostgreSQL\\17\\bin\\createdb.exe" -h ${dbHost} -p ${dbPort} -U ${dbUser} ${dbName}`;

    try {
      execSync(createDbCommand, { stdio: 'inherit' });
      console.log(`✅ Database '${dbName}' created successfully!`);
    } catch (error) {
      if (error.message.includes('already exists')) {
        console.log(`ℹ️  Database '${dbName}' already exists, continuing...`);
      } else {
        console.log(`⚠️  Could not create database automatically. Please create it manually:`);
        console.log(`   createdb -U ${dbUser} ${dbName}`);
      }
    }

    // Create .env file
    const envContent = `# Local Development Environment Configuration
# Generated by setup-local-db.js

# Database Configuration (Local PostgreSQL)
DATABASE_URL="${connectionString}"

# Session Secret (generate a secure random string for production)
SESSION_SECRET="dev-session-secret-${Math.random().toString(36).substring(2)}"

# Development flags
NODE_ENV=development

# Optional: Google AI API Key for testing AI features
# GOOGLE_AI_API_KEY="your-google-ai-api-key-here"

# Optional: Stripe keys for payment testing
# STRIPE_SECRET_KEY="sk_test_..."
# VITE_STRIPE_PUBLIC_KEY="pk_test_..."

# Optional: SendGrid for email testing
# SENDGRID_API_KEY="SG..."

# OAuth Configuration (for Replit auth - optional in local dev)
# REPL_ID="your-repl-id"
# REPLIT_DOMAINS="localhost:3000"
`;

    writeFileSync('.env', envContent);
    console.log('✅ Created .env file with database configuration');

    // Test database connection
    console.log('\n🧪 Testing database connection...');
    const testCommand = dbPassword
      ? `"C:\\Program Files\\PostgreSQL\\17\\bin\\psql.exe" -h ${dbHost} -p ${dbPort} -U ${dbUser} -d ${dbName} -c "SELECT version();"`
      : `"C:\\Program Files\\PostgreSQL\\17\\bin\\psql.exe" -h ${dbHost} -p ${dbPort} -U ${dbUser} -d ${dbName} -c "SELECT version();"`;

    try {
      execSync(testCommand, { stdio: 'inherit' });
      console.log('✅ Database connection successful!');
    } catch (error) {
      console.log('⚠️  Database connection test failed. You may need to configure authentication.');
      console.log('   Check your PostgreSQL pg_hba.conf file or set up password authentication.');
    }

    // Run migrations
    console.log('\n📦 Running database migrations...');
    try {
      execSync('npm run db:push', { stdio: 'inherit' });
      console.log('✅ Database schema created successfully!');
    } catch (error) {
      console.log('⚠️  Migration failed. You can run it manually later with: npm run db:push');
    }

    console.log('\n🎉 Setup complete!');
    console.log('\nNext steps:');
    console.log('1. Start your development server: npm run dev');
    console.log('2. Visit http://localhost:3000');
    console.log('3. Test the authentication flow');
    console.log('\nIf you encounter issues:');
    console.log('- Check that PostgreSQL service is running');
    console.log('- Verify your database credentials');
    console.log('- Run migrations manually: npm run db:push');

  } catch (error) {
    console.error('❌ Setup failed:', error.message);
    console.log('\nTroubleshooting:');
    console.log('1. Make sure PostgreSQL is installed and running');
    console.log('2. Check your database credentials');
    console.log('3. Verify network connectivity to database');
  }

  rl.close();
}

// Run setup if this file is executed directly
if (import.meta.url === `file://${process.argv[1]}`) {
  setupDatabase().catch(console.error);
}

export default setupDatabase;

